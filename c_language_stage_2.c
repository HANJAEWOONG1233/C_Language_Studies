# include <stdio.h>

int main(void) // 입력형태void 출력 형태:int, main이라는 함수
{
    printf("Hello world \n");
    printf("my age: %d \n",20); //%d를 서식문자라 하고ㅗ \n 은 개행의 의미를 가진다. 20이라는 인자가 %d 서식 문자 안으로 들어간다.
    printf("%d is my point \n",100);
    printf("good \nmorning \neverybody");
    return 0; //함수 종료, 값 변환
}

